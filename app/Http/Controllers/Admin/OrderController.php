            $order = Order::with(['user','transaction.payment_method','order_tracking.shipping_method'])->find($id);
            //dd($order);
            return view('order.admin.show')->with([
                'order' => $order
            ]);
        }
        return redirect()->back()->with('error','unauthorized access');
    }

